    #[test]
    fn test_parse_block_response() {
        let json = serde_json::json!({
            "blockTime": 1_700_000_000i64,
            "transactions": [
                {
                    "transaction": {
                        "message": {
                            "accountKeys": ["key1", "key2", "TokenkegQfeZyiNwAJbNbGKPFXCWuBvf9Ss623VQ5DA"],
                            "instructions": [
                                {
                                    "programIdIndex": 2,
                                    "data": "3Bxs4h24hBtQy9rw",
                                    "accounts": [0, 1]
                                }
                            ]
                        },
                        "signatures": ["5eykt4UsFv8P8NJdTREpY1vzqKqZKvdp"]
                    },
                    "meta": {
                        "err": null,
                        "innerInstructions": [
                            {
                                "index": 0,
                                "instructions": [
                                    {
                                        "programIdIndex": 1,
                                        "data": "11",
                                        "accounts": [0]
                                    }
                                ]
                            }
                        ]
                    }
                },
                {
                    "transaction": {
                        "message": {
                            "accountKeys": ["keyA"],
                            "instructions": []
                        },
                        "signatures": ["failedSig"]
                    },
                    "meta": {
                        "err": {"InstructionError": [0, "Custom"]},
                        "innerInstructions": []
                    }
                }
            ]
        });

        // With index_failed_txs = false, should filter out the failed tx
        let block = parse_block(42, json.clone(), false).expect("parse_block failed");

        assert_eq!(block.slot, 42);
        assert_eq!(block.block_time, Some(1_700_000_000));
        assert_eq!(block.transactions.len(), 1);
        assert_eq!(
            block.transactions[0].signature,
            "5eykt4UsFv8P8NJdTREpY1vzqKqZKvdp"
        );
        assert!(block.transactions[0].success);
        assert_eq!(block.transactions[0].instructions.len(), 1);
        assert_eq!(block.transactions[0].instructions[0].program_id_index, 2);
        assert_eq!(block.transactions[0].inner_instructions.len(), 1);
        assert_eq!(
            block.transactions[0].inner_instructions[0]
                .instructions
                .len(),
            1
        );

        // With index_failed_txs = true, should keep both
        let block_all = parse_block(42, json, true).expect("parse_block failed");

        assert_eq!(block_all.transactions.len(), 2);
        assert!(!block_all.transactions[1].success);
    }

