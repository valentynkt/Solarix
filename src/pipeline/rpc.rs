#[async_trait]
impl BlockSource for RpcClient {
    async fn get_blocks(&self, start_slot: u64, end_slot: u64) -> Result<Vec<u64>, PipelineError> {
        let chunks = compute_block_chunks(start_slot, end_slot);
        let mut all_slots = Vec::new();

        for (chunk_start, chunk_end) in chunks {
            let params = serde_json::json!([
                chunk_start,
                chunk_end,
                { "commitment": "finalized" }
            ]);

            let value = self.rpc_request("getBlocks", params).await?;
            let slots: Vec<u64> = serde_json::from_value(value).map_err(|e| {
                PipelineError::RpcFailed(format!("getBlocks response parse failed: {e}"))
            })?;
            all_slots.extend(slots);
        }

        Ok(all_slots)
    }

    async fn get_block(&self, slot: u64) -> Result<Option<RpcBlock>, PipelineError> {
        let params = serde_json::json!([
            slot,
            {
                "encoding": "json",
                "transactionDetails": "full",
                "maxSupportedTransactionVersion": 0,
                "commitment": "finalized"
            }
        ]);

        match self.rpc_request("getBlock", params).await {
            Ok(value) => {
                let block = parse_block(slot, value, self.index_failed_txs)?;
                Ok(Some(block))
            }
            Err(PipelineError::SlotSkipped(_)) => {
                debug!(slot, "slot skipped");
                Ok(None)
            }
            Err(e) => Err(e),
        }
    }

    async fn get_slot(&self) -> Result<u64, PipelineError> {
        let params = serde_json::json!([{ "commitment": "finalized" }]);
        let value = self.rpc_request("getSlot", params).await?;
        serde_json::from_value(value)
            .map_err(|e| PipelineError::RpcFailed(format!("getSlot response parse failed: {e}")))
    }
}

