#[derive(Deserialize)]
struct RawGetBlockResult {
    #[serde(rename = "blockTime")]
    block_time: Option<i64>,
    transactions: Option<Vec<RawBlockTransaction>>,
}

#[derive(Deserialize)]
struct RawBlockTransaction {
    transaction: RawTransactionPayload,
    meta: Option<RawTransactionMeta>,
}

#[derive(Deserialize)]
struct RawTransactionPayload {
    message: RawMessage,
    signatures: Vec<String>,
}

#[derive(Deserialize)]
struct RawMessage {
    #[serde(rename = "accountKeys")]
    account_keys: Vec<String>,
    instructions: Vec<RawInstruction>,
}

#[derive(Deserialize)]
struct RawInstruction {
    #[serde(rename = "programIdIndex")]
    program_id_index: u8,
    data: String, // base58-encoded
    accounts: Vec<u8>,
}

#[derive(Deserialize)]
struct RawTransactionMeta {
    err: Option<serde_json::Value>,
    #[serde(rename = "innerInstructions")]
    inner_instructions: Option<Vec<RawInnerInstructionGroup>>,
    #[serde(rename = "loadedAddresses", default)]
    loaded_addresses: Option<RawLoadedAddresses>,
}

/// Addresses resolved from v0 transaction address lookup tables.
#[derive(Deserialize)]
struct RawLoadedAddresses {
    #[serde(default)]
    writable: Vec<String>,
    #[serde(default)]
    readonly: Vec<String>,
}

#[derive(Deserialize)]
struct RawInnerInstructionGroup {
    index: u8,
    instructions: Vec<RawInstruction>,
}

