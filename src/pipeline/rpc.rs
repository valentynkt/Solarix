/// Trait for fetching blocks from Solana RPC.
#[async_trait]
pub trait BlockSource: Send + Sync {
    /// Get confirmed block slot numbers within a range.
    async fn get_blocks(&self, start_slot: u64, end_slot: u64) -> Result<Vec<u64>, PipelineError>;

    /// Fetch a block by slot. Returns `None` for skipped/empty slots.
    async fn get_block(&self, slot: u64) -> Result<Option<RpcBlock>, PipelineError>;

    /// Get the current highest processed slot.
    async fn get_slot(&self) -> Result<u64, PipelineError>;
}

/// Trait for fetching account data from Solana RPC.
#[async_trait]
pub trait AccountSource: Send + Sync {
    /// Fetch all account pubkeys for a program (using dataSlice trick).
    async fn get_program_accounts(&self, program_id: &str) -> Result<Vec<String>, PipelineError>;

    /// Fetch multiple accounts by pubkey (auto-batches in chunks of 100).
    async fn get_multiple_accounts(
        &self,
        pubkeys: &[String],
    ) -> Result<Vec<RpcAccountInfo>, PipelineError>;
}

